// Em JavaScript, existem muitos operadores diferentes que você pode usar para realizar operações em valores e 
// variáveis. Aqui estão alguns dos operadores mais comuns em JavaScript:

// Operadores Aritméticos

// + Adição
// - Subtração
// * Multiplicação
// / Divisão
// % Resto da divisão
// ++ Incremento
// -- Decremento

// Operadores de Atribuição

// = Atribuição simples
// += Adição e atribuição
// -= Subtração e atribuição
// *= Multiplicação e atribuição
// /= Divisão e atribuição
// %= Resto da divisão e atribuição

// Operadores de Comparação

// == Igual a
// != Diferente de
// === Estritamente igual a
// !== Estritamente diferente de
// > Maior que
// < Menor que
// >= Maior ou igual a
// <= Menor ou igual a

// Operadores Lógicos

// && E lógico
// || Ou lógico
// ! Negação lógica

// Operadores Bitwise

// & AND bitwise
// | OR bitwise
// ~ NOT bitwise
// ^ XOR bitwise
// << Shift left
// >> Shift right
// >>> Shift right sem sinal

// Esses são apenas alguns dos operadores mais comuns em JavaScript. Há também outros operadores,
// como operadores ternários (? :), operadores de exponenciação (**), operadores de acesso a propriedades (. e []), entre outros.